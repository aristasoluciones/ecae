<p><strong>Ampliamos la convocatoria para el registro!</strong>&nbsp;</p>
<p>Solo falta un paso m&aacute;s para concluir tu registro. Deber&aacute;s presentarte en las oficinas de tu Consejo Municipal Electoral m&aacute;s cercano entre el <strong><u>07 y 11 de abril de 2024</u></strong>, llevando la siguiente documentaci&oacute;n:&nbsp;</p>
<p>Ubica la direcci&oacute;n de tu Consejo Municipal m&aacute;s cercano aqu&iacute;:&nbsp;</p>
<p><a href="https://www.iepc-chiapas.org.mx/organos-desconcentrados-2024">https://www.iepc-chiapas.org.mx/organos-desconcentrados-2024</a>&nbsp;</p>
<p>Descarga tu gu&iacute;a de estudio aqu&iacute;:&nbsp;</p>
<p><a href="https://www.iepc-chiapas.org.mx/sel-y-cael-pelo2024">https://www.iepc-chiapas.org.mx/sel-y-cael-pelo2024</a>&nbsp;</p>
<p>Documentaci&oacute;n que deber&aacute;s presentar:&nbsp;</p>
<ol>
	<li>Solicitud correctamente llenada y firmada.</li>
	<li>Acta de nacimiento (original o copia certificada y copia simple), o en su caso, Carta de Naturalizaci&oacute;n.</li>
	<li>Credencial para Votar Vigente o comprobante de tr&aacute;mite.</li>
	<li>Comprobante de domicilio (Agua, luz, tel&eacute;fono, constancia de domicilio expedida por autoridad competente).</li>
	<li>Constancia de estudios (no tira de materias) acreditando, como m&iacute;nimo, el nivel de educaci&oacute;n media b&aacute;sica (secundaria).</li>
	<li>Declaratoria bajo protesta de decir verdad (firmada).</li>
	<li>Copia de la Clave &Uacute;nica del Registro de Poblaci&oacute;n (CURP).</li>
	<li>Copia del Registro Federal de Contribuyentes (RFC).&nbsp;</li>
</ol>
<p>En caso de ser contratado/a, ser&aacute; necesario que entregues la siguiente documentaci&oacute;n:&nbsp;</p>
<ol>
	<li>Tres fotograf&iacute;as tama&ntilde;o infantil a color o en blanco y negro.&nbsp;</li>
</ol>
<p>Adem&aacute;s, algunos documentos opcionales complementarios que pueden fortalecer tu perfil son:&nbsp;</p>
<ol>
	<li>Carta que acredite tu experiencia como docente, manejo o trato de grupos de personas.</li>
	<li>Constancia de participaci&oacute;n en alg&uacute;n Proceso Electoral Concurrente, Federal o Local.</li>
	<li>Licencia de manejo vigente.&nbsp;</li>
</ol>
<p>Agradecemos tu inter&eacute;s en participar como Supervisor/a Asistente Electoral Local (SEL) y/o Capacitador/a Asistente Electoral Local (CAEL) en este Proceso Electoral Local Ordinario 2024.&nbsp;</p>
<p>Es esencial que tengas presente las siguientes etapas del proceso de selecci&oacute;n:&nbsp;</p>
<ol>
	<li><strong>Pl&aacute;tica de Inducci&oacute;n:</strong> En esta fase, recibir&aacute;s informaci&oacute;n detallada sobre las responsabilidades y expectativas del cargo que est&aacute;s considerando.&nbsp;</li>
</ol>
<ol start="2">
	<li><strong>Revisi&oacute;n Documental:</strong> Verificaremos la documentaci&oacute;n presentada para asegurarnos de que cumple con los requisitos establecidos en la convocatoria.&nbsp;</li>
</ol>
<ol start="3">
	<li><strong>Examen:</strong> Este evaluar&aacute; tus conocimientos relacionados con el proceso electoral y las funciones del puesto.&nbsp;</li>
</ol>
<ol start="4">
	<li><strong>Entrevista:</strong> Brindar&aacute; la oportunidad de compartir m&aacute;s acerca de tu experiencia, habilidades y motivaci&oacute;n para desempe&ntilde;ar este rol.&nbsp;</li>
</ol>
